package main.java;


public class OddOrEven {

    public static boolean isOddNumber(int x) {
        if (x % 2 == 0) {
            return true;
        } else {
            return false;
        }
    }

    public static boolean isEvenNumber(int x) {
        if (x % 2 != 0) {
            return true;
        } else {
            return false;
        }
    }
}

